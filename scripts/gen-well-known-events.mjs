import { getExtension } from '@bufbuild/protobuf'
import { kind } from '@buf/fivebits_pug.bufbuild_es/common/events/v1/options_pb.js'
import { readFileSync, writeFileSync } from 'node:fs'
import { dirname, join } from 'node:path'
import { fileURLToPath } from 'node:url'

const EVENT_MODULES = [
  'api_events',
  'app_events',
  'auth_events',
  'billing_events',
  'chat_events',
  'commerce_events',
  'discovery_events',
  'error_events',
  'file_events',
  'form_events',
  'integration_events',
  'invitation_events',
  'media_events',
  'navigation_events',
  'notification_events',
  'social_events',
  'support_events',
  'workspace_events',
]

const PACKAGE_PATH = '@buf/fivebits_pug.bufbuild_es/common/events/v1'

const __dirname = dirname(fileURLToPath(import.meta.url))
const repoRoot = join(__dirname, '..')

const bufVersion = JSON.parse(readFileSync(join(repoRoot, 'package.json'), 'utf8')).dependencies[
  '@buf/fivebits_pug.bufbuild_es'
]

const entries = []
for (const mod of EVENT_MODULES) {
  const m = await import(`${PACKAGE_PATH}/${mod}_pb.js`)
  for (const [name, schema] of Object.entries(m)) {
    if (!name.endsWith('PropertiesSchema')) continue
    const opts = schema?.proto?.options
    if (!opts) continue
    const k = getExtension(opts, kind)
    if (k) entries.push({ kind: k, module: mod, schema: name })
  }
}

entries.sort((a, b) => a.kind.localeCompare(b.kind))

const byModule = new Map()
for (const e of entries) {
  if (!byModule.has(e.module)) byModule.set(e.module, [])
  byModule.get(e.module).push(e.schema)
}

const importBlock = [...byModule.entries()]
  .sort(([a], [b]) => a.localeCompare(b))
  .map(([mod, schemas]) => {
    const lines = [...schemas].sort().map(s => `  ${s},`)
    return `import {\n${lines.join('\n')}\n} from '${PACKAGE_PATH}/${mod}_pb.js'`
  })
  .join('\n')

const mapBlock = entries.map(e => `  ${e.kind}: ${e.schema},`).join('\n')

const fileContent = `// AUTOGENERATED — do not edit.
// Run \`node scripts/gen-well-known-events.mjs\` to regenerate (also runs as part of \`npm run build\`).
// Source: @buf/fivebits_pug.bufbuild_es@${bufVersion}

/* eslint-disable */
${importBlock}

export const wellKnownSchemas = Object.freeze({
${mapBlock}
} as const)
`

const outPath = join(repoRoot, 'src', 'well-known-events.generated.ts')
writeFileSync(outPath, fileContent)
console.log(`Wrote ${entries.length} well-known events to ${outPath}`)
